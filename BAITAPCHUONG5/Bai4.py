'''Một số hàm quan trọng trong xử lý chuỗi Python:

len(s): trả về độ dài chuỗi.

s.lower() / s.upper(): chuyển chữ thường / hoa.

s.strip(): xóa khoảng trắng đầu và cuối.

s.split(sep): tách chuỗi thành danh sách.

sep.join(list): nối các phần tử trong list thành chuỗi.

s.replace(old, new): thay thế chuỗi con.

s.find(sub) / s.index(sub): tìm vị trí chuỗi con.

s.startswith(x) / s.endswith(x): kiểm tra bắt đầu / kết thúc bằng chuỗi con.'''